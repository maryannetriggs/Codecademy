// My Age
const myAge = 31

// Conversion Factor for Early Years
let earlyYears = 2
earlyYears *= 10.5

// Calculation of Number of Later Years
let laterYears = myAge - 2

// Conversion Factor for Later Years
laterYears *= 4

// Addition of Early and Later Years
let myAgeInDogYears = earlyYears + laterYears

// My Name in Lower Case
let myName = 'Mary-Anne'.toLowerCase()

// Statement of My Age in Dog Years
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)